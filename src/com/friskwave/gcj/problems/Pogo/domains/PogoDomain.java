package com.friskwave.gcj.problems.Pogo.domains;

public class PogoDomain {
	
	private int caseNumber;
	private String caseData;
	private long caseX;
	private long caseY;
	
	public int getCaseNumber() {
		return caseNumber;
	}
	
	public void setCaseNumber(int caseNumber) {
		this.caseNumber = caseNumber;
	}
	
	public String getCaseData() {
		return caseData;
	}
	
	public void setCaseData(String caseData) {
		this.caseData = caseData;
	}
	
	public long getCaseX() {
		return caseX;
	}
	
	public void setCaseX(long caseX) {
		this.caseX = caseX;
	}
	
	public long getCaseY() {
		return caseY;
	}
	
	public void setCaseY(long caseY) {
		this.caseY = caseY;
	}
}