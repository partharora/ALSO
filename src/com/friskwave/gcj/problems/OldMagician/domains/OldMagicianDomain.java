package com.friskwave.gcj.problems.OldMagician.domains;

public class OldMagicianDomain {
	
	private int caseNumber;
	private long blackBallCount;
	private long whiteBallCount;
	
	public int getCaseNumber() {
		return caseNumber;
	}
	
	public void setCaseNumber(int caseNumber) {
		this.caseNumber = caseNumber;
	}

	public long getBlackBallCount() {
		return blackBallCount;
	}

	public void setBlackBallCount(long blackBallCount) {
		this.blackBallCount = blackBallCount;
	}

	public long getWhiteBallCount() {
		return whiteBallCount;
	}

	public void setWhiteBallCount(long whiteBallCount) {
		this.whiteBallCount = whiteBallCount;
	}
}