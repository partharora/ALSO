package com.friskwave.gcj.problems.reversewords.domains;

public class ReverseWordDomain {
	
	private int caseNumber;
	private String caseWordList;
	private String[] caseWordListArr;
	
	public int getCaseNumber() {
		return caseNumber;
	}

	public void setCaseNumber(int caseNumber) {
		this.caseNumber = caseNumber;
	}
	
	public String getCaseWordList() {
		return caseWordList;
	}
	
	public void setCaseWordList(String caseWordList) {
		this.caseWordList = caseWordList;
	}
	
	public String[] getCaseWordListArr() {
		return caseWordListArr;
	}
	
	public void setCaseWordListArr(String[] caseWordListArr) {
		this.caseWordListArr = caseWordListArr;
	}
}